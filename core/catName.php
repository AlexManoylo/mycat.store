function catName($unusualNames, $catAdjectives, $catDescriptions)
{
    shuffle($unusualNames);
    shuffle($catAdjectives);
    shuffle($catDescriptions);

    $catName = $unusualNames[array_rand($unusualNames)];
    $catAdjective = $catAdjectives[array_rand($catAdjectives)];

    if ($catName) {
        $lastChar = mb_substr($catName, -1, 1,);
        $lastCharAdjective = mb_substr($catAdjective, -2, 2,);

        if (($lastChar == 'а' || $lastChar == 'я') && ($lastCharAdjective == 'ый' || $lastCharAdjective == 'ий')) {
            $catAdjective = str_replace('ий', 'ая', $catAdjective);
            $catAdjective = str_replace('ый', 'ая', $catAdjective);
        }

        //$catFullName = Случайно имя кота из массива
        //$$catDescription = Описание
        $catFullName = $catName . ' ' . $catAdjective;

    }
    $catDescription = $catDescriptions[array_rand($catDescriptions)];
    return [
        'name'=> $catFullName,
        'description'=> $catDescription,
        ];
}

